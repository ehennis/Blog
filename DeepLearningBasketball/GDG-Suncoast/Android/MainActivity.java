    @Override
    public void onClick(View v) {
        switch (v.getId()) {
            case R.id.btnPredict:
                if(spnAway.getSelectedItem() == null || spnAway.getSelectedItem().toString().length() == 0){
                    //Need to have a value
                    return;
                }
                if(spnHome.getSelectedItem() == null || spnHome.getSelectedItem().toString().length() == 0){
                    //Need to have a value
                    return;
                }
                //Grab the team names
                String hm = spnHome.getSelectedItem().toString();
                String aw = spnAway.getSelectedItem().toString();
                //Grab their stats
                String hm_stats = teamStats.get(hm);
                String aw_stats = teamStats.get(aw);
                //Split to generate values
                String[] hm_entries = hm_stats.split(",");
                String[] aw_entries = hm_stats.split(",");
                String hm_HomeScoringAverage = hm_entries[0];
                String hm_HomeDefenseAverage = hm_entries[1];
                String aw_AwayScoringAverage = aw_entries[2];
                String aw_AwayDefenseAverage = aw_entries[3];
                //Convert to numbers and create array
                float[] input = BuildInputArray(
                        Float.parseFloat(hm_HomeScoringAverage),
                        Float.parseFloat(hm_HomeDefenseAverage),
                        Float.parseFloat(aw_AwayScoringAverage),
                        Float.parseFloat(aw_AwayDefenseAverage));
                try {
                    //Generate TensorFlow Lite Interpreter
                    Interpreter intp = new Interpreter(TFLiteModel);
                    //Run the interpreter
                    float[][] out = new float[1][1];
                    intp.run(input, out);
                    intp.close();
                    float results = out[0][0];
                    //Set Text
                    String txt = (results > 0 ? "The home team will win by " + String.format("%.2f", results) : "The away team will win by " + String.format("%.2f", (results * -1)));
                    txtResults.setText(txt);
                }
                catch (Exception ex){
                    Toast.makeText(this, ex.getMessage(), Toast.LENGTH_LONG);

                }
                break;
        }
    }

